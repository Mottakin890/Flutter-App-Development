void main(){
  //List insertion and addition
  var fruits = ['Apple', 'Banana', 'Cherry'];
  fruits.add('Mango'); // Adding a new fruit to the list
  fruits.insert(1, 'Orange'); // Inserting 'Orange' at index 1
  print(fruits); // Output: [Apple, Orange, Banana, Cherry, Mango]
  // This code demonstrates how to insert and add elements to a list in Dart.
}